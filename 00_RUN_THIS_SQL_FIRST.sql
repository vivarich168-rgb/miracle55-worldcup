-- sync predictions match_key จาก match_id
update public.predictions p
set match_key = m.match_key
from public.matches m
where p.match_key is null and p.match_id = m.id;

-- reset duplicate/index แบบปลอดภัย
DROP INDEX IF EXISTS predictions_user_match_key_unique;

DELETE FROM public.predictions
WHERE id IN (
    SELECT id
    FROM (
        SELECT id,
               ROW_NUMBER() OVER (PARTITION BY user_id, match_key ORDER BY updated_at DESC NULLS LAST, created_at DESC NULLS LAST, id DESC) AS rn
        FROM public.predictions
        WHERE user_id IS NOT NULL AND match_key IS NOT NULL
    ) t
    WHERE rn > 1
);

CREATE UNIQUE INDEX IF NOT EXISTS predictions_user_match_key_unique
ON public.predictions(user_id, match_key);

CREATE UNIQUE INDEX IF NOT EXISTS matches_match_key_unique
ON public.matches(match_key);
