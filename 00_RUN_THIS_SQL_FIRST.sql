-- เติม match_key ให้ matches เดิม
update public.matches set match_key = 'm-' || id::text where match_key is null and id between 1 and 16;

-- เติม actual_winner ให้รายการที่มีผลแล้วแต่ยังว่าง
update public.matches
set actual_winner = case
    when actual_score_a > actual_score_b then team_a
    when actual_score_b > actual_score_a then team_b
    else actual_winner
end
where actual_score_a is not null and actual_score_b is not null and (actual_winner is null or actual_winner = '');

